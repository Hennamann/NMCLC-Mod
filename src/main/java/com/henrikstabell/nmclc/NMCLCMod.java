public class NMCLCMod extends DummyModContainer {

    public static final String version = "1.2.0";

    public static Logger logger;

    public NMCLCMod() {

        super(new ModMetadata());
        ModMetadata meta = getMetadata();
        meta.modId = "nmclc";
        meta.name = "NMCLC (NoMoreCapsLockCommands)";
        meta.version = version;
        meta.authorList = Arrays.asList("Hennamann");
        meta.description = "Converts commands to lowercase to save time when typing commands while capslock is on.";
        meta.url = "https://github.com/hennamann/NMCLC-Mod";
        meta.autogenerated = false;
    }

    @Override
    public boolean registerBus(EventBus bus, LoadController controller) {
        bus.register(this);
        return true;
    }

    @Subscribe
    public void modConstruction(FMLConstructionEvent evt){}

    @Subscribe
    public void preInit(FMLPreInitializationEvent evt) {}

    @Subscribe
    public void init(FMLInitializationEvent evt) {}

    @Subscribe
    public void postInit(FMLPostInitializationEvent evt) {}
}